package com.podval.models;

import javax.persistence.*;

@Entity
@Table(name = "books")
public class Book implements IModel {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;

    @Column(name = "name")
    private String name;

    @Column(name = "price")
    private int price;

    @Column(name = "warehouse")
    private String warehouse;

    @Column(name = "count")
    private int count;

    public Book(){
    }

    public Book(String name, int price, String warehouse, int count) {
        this.name = name;
        this.price = price;
        this.warehouse = warehouse;
        this.count = count;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getWarehouse() {
        return warehouse;
    }

    public void setWarehouse(String warehouse) {
        this.warehouse = warehouse;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    @Override
    public String toString() {
        return "Book{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", price=" + price +
                ", warehouse='" + warehouse + '\'' +
                ", count=" + count +
                '}';
    }
}
